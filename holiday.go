package goliday

import (
	"strconv"
	"strings"
)

// Holiday holids holiday information.
type Holiday interface {
	// Year of holiday.
	Year() int

	// Month of holiday.
	Month() int

	// Day of holiday.
	Day() int

	// Ymd of holiday.
	Ymd() string

	// Week of holiday.
	Week() string

	// WeekEn of holiday.
	WeekEn() string

	// Name of holiday.
	Name() string

	// NameEn of holiday.
	NameEn() string
}

// Holidays is Holiday interface slice
type Holidays []Holiday

type holiday struct {
	ymd    string
	week   string
	weekEn string
	name   string
	nameEn string
}

func (h *holiday) Year() int {
	n, _ := strconv.Atoi(strings.Split(h.ymd, "-")[0])
	return n
}

func (h *holiday) Month() int {
	n, _ := strconv.Atoi(strings.Split(h.ymd, "-")[1])
	return n
}

func (h *holiday) Day() int {
	n, _ := strconv.Atoi(strings.Split(h.ymd, "-")[2])
	return n
}

func (h *holiday) Ymd() string    { return h.ymd }
func (h *holiday) Week() string   { return h.week }
func (h *holiday) WeekEn() string { return h.weekEn }
func (h *holiday) Name() string   { return h.name }
func (h *holiday) NameEn() string { return h.nameEn }

