func (hds Holidays) contains(ops *evaluateOption) bool {
	for _, h := range hds {
		exists := false

		if ops.year != -1 {
			if h.Year() != ops.year {
				continue
			}
			exists = true
		}

		if ops.month != -1 {
			if h.Month() != ops.month {
				continue
			}
			exists = true
		}

		if ops.day != -1 {
			if h.Day() != ops.day {
				continue
			}
			exists = true
		}

		if exists {
			return exists
		}
	}
	return false
}

func (hds Holidays) holidays(ops evaluateOption) []Holiday {
	holidays := []Holiday{}

	for _, h := range hds {
		exists := false

		if ops.year != -1 {
			if h.Year() != ops.year {
				continue
			}
			exists = true
		}

		if ops.month != -1 {
			if h.Month() != ops.month {
				continue
			}
			exists = true
		}

		if ops.day != -1 {
			if h.Day() != ops.day {
				continue
			}
			exists = true
		}

		if exists {
			holidays = append(holidays, h)
		}
	}
	return holidays
}

func (hds Holidays) inMonth(year, month int) []Holiday {
	ymstr := toString(year) + "-" + toString(month)

	results := []Holiday{}
	for _, h := range hds {
		if h.Ymd()[0:len(ymstr)] == ymstr {
			results = append(results, h)
		}
	}
	return results
}

func (hds Holidays) inYear(year int) []Holiday {
	ystr := toString(year)

	results := []Holiday{}
	for _, h := range hds {
		if h.Ymd()[0:len(ystr)] == ystr {
			results = append(results, h)
		}
	}
	return results
}

func formatYmd(year, month, day int) string {
	ystr, mstr, dstr := toString(year), toString(month), toString(day)
	return ystr + "-" + mstr + "-" + dstr
}

func toString(num int) string {
	str := strconv.Itoa(num)
	if len(str) == 1 {
		return "0" + str
	}
	return str
}
